// ---------------------------------------------------------------------------
// Go-Live Readiness Engine — Odoo 19 Implementation Control Platform
// ---------------------------------------------------------------------------
//
// Purpose:
//   Derives go-live readiness state from governed checkpoint status, active
//   blockers, deferment records, stage routing, and target context. Produces
//   the readiness_state container (§1.8 of the runtime state contract).
//
// Governing constraints:
//   - specs/runtime_state_contract.md §1.8 (readiness_state shape)
//   - specs/runtime_state_contract.md §1.9 (blockers shape)
//   - specs/checkpoint_engine.md §2 (checkpoint_class enum, status enum)
//   - specs/stage_routing_engine.md (stage routing output shape)
//
// Hard rules:
//   R1  Reads only: checkpoints, validated_checkpoints, blockers,
//       stage_routing, and named projectState fields where explicitly required.
//       No checkpoint generation, no status mutation, no blocker re-computation.
//   R2  Checkpoint status completeness is evaluated as:
//       Complete | Deferred → satisfied; all other statuses → incomplete.
//   R3  Blocking-class checkpoints: Foundational | Domain_Required | Go_Live.
//       Any blocking-class checkpoint not in {Complete, Deferred} is a
//       configuration gap and prevents go-live readiness.
//   R4  Recommended and Optional checkpoints never silently block readiness.
//       They are reported in unresolved_warnings only.
//   R5  Active blockers with severity "critical" on blocking-class checkpoints
//       are a separate blocking gate (blocked_checkpoints).
//       Standard-severity blockers on non-blocking-class checkpoints are
//       advisory (unresolved_warnings).
//   R6  Operational readiness gates (non-production environment, undefined
//       odoosh branch target) are evaluated only when the relevant target_context
//       fields are explicitly present. No inference from absent fields.
//   R7  Branch/environment gaps apply only when deployment_type === "odoosh"
//       AND the relevant field is explicitly set to a non-production value
//       (environment) or is null/absent (branch target).
//   R8  Training availability is advisory only. Training blocks readiness only
//       when projectState.project_identity.required_training_confirmed is
//       explicitly false. If the field is absent, training is advisory.
//   R9  Production readiness cannot be claimed from non-production evidence
//       alone. When odoosh_environment_type is a known non-production value
//       ("staging" | "development" | "test") an operational gap is recorded.
//   R10 Configuration complete ≠ operationally ready. These are distinct states
//       reflected in go_live_readiness.
//   R11 deferred_checkpoints records all checkpoints with status "Deferred"
//       regardless of checkpoint_class, with deferment record reference where
//       available. This is informational; Deferred satisfies the R2 gate.
//   R12 Output is deterministic: same inputs always produce same outputs.
//       No timestamps or IDs are generated by this engine.
//   R13 recommendation_issued, recommendation_issued_at, and
//       recommendation_issued_by are always false/null from this engine.
//       Recommendation issuance requires an explicit actor action; this
//       engine only determines whether a recommendation CAN be issued.
//   R14 readiness_gap records preserve full traceability: source_type,
//       source_id, and the originating checkpoint_id (where applicable).
// ---------------------------------------------------------------------------

import { ODOO_VERSION } from "./constants.js";

if (ODOO_VERSION !== "19") {
  throw new Error(
    `golive-readiness-engine: ODOO_VERSION must be "19", got "${ODOO_VERSION}". Halting module init.`
  );
}

// ---------------------------------------------------------------------------
// Engine version — increment on any rule change
// ---------------------------------------------------------------------------

export const GOLIVE_READINESS_ENGINE_VERSION = "1.0.0";

// ---------------------------------------------------------------------------
// Constant enumerations
// ---------------------------------------------------------------------------

export const GO_LIVE_READINESS_STATUS = Object.freeze({
  READY:                                       "Ready",
  NOT_READY:                                   "Not_Ready",
  CONFIGURATION_COMPLETE_NOT_OPERATIONALLY_READY: "Configuration_Complete_Not_Operationally_Ready",
});

export const READINESS_GAP_SOURCE_TYPE = Object.freeze({
  CHECKPOINT:     "checkpoint",
  BLOCKER:        "blocker",
  TRAINING:       "training",
  TARGET_CONTEXT: "target_context",
});

export const READINESS_TRAINING_STATUS = Object.freeze({
  OK:                       "OK",
  AVAILABLE_NOT_CONFIRMED:  "Training_Available_Not_Confirmed",
});

// Checkpoint classes that gate go-live readiness (R3)
const BLOCKING_CHECKPOINT_CLASSES = Object.freeze(
  new Set(["Foundational", "Domain_Required", "Go_Live"])
);

// Statuses satisfying the checkpoint completeness gate (R2)
const COMPLETION_STATUSES = Object.freeze(new Set(["Complete", "Deferred"]));

// Known non-production Odoo.sh environment types (R9)
const NON_PRODUCTION_ODOOSH_ENV_TYPES = Object.freeze(
  new Set(["staging", "development", "test"])
);

// ---------------------------------------------------------------------------
// Output factories
// ---------------------------------------------------------------------------

/**
 * Creates a single readiness gap record with full traceability fields.
 *
 * @param {object} params
 * @param {string}      params.source_type        - READINESS_GAP_SOURCE_TYPE constant
 * @param {string|null} params.source_id          - primary key of the originating record
 * @param {string|null} params.checkpoint_id      - originating checkpoint (null for target_context)
 * @param {string|null} params.checkpoint_class   - class of the originating checkpoint
 * @param {string|null} params.domain             - domain of the originating checkpoint
 * @param {string|null} params.stage_id           - stage assignment from stage_routing
 * @param {string|null} params.blocker_id         - blocker_id when source_type === "blocker"
 * @param {string|null} params.blocker_type       - blocker_type when source_type === "blocker"
 * @param {string|null} params.deferment_id       - deferment_id when checkpoint is Deferred
 * @param {boolean}     params.has_owner_signoff  - deferment owner_signoff flag
 * @param {string|null} params.gap_reason         - human-readable explanation
 * @returns {object} readiness_gap
 */
export function createReadinessGap({
  source_type = null,
  source_id = null,
  checkpoint_id = null,
  checkpoint_class = null,
  domain = null,
  stage_id = null,
  blocker_id = null,
  blocker_type = null,
  deferment_id = null,
  has_owner_signoff = false,
  gap_reason = null,
} = {}) {
  return {
    source_type,
    source_id,
    checkpoint_id,
    checkpoint_class,
    domain,
    stage_id,
    blocker_id,
    blocker_type,
    deferment_id,
    has_owner_signoff,
    gap_reason,
  };
}

/**
 * Creates the readiness_state container returned by computeGoLiveReadiness().
 * Shape matches §1.8 of the runtime contract exactly.
 *
 * @param {object} params
 * @param {string|null} params.go_live_readiness
 * @param {string|null} params.readiness_reason
 * @param {Array}       params.incomplete_critical_checkpoints
 * @param {Array}       params.blocked_checkpoints
 * @param {Array}       params.deferred_checkpoints
 * @param {Array}       params.unresolved_warnings
 * @param {string|null} params.training_status
 * @param {boolean}     params.recommendation_issued
 * @param {string|null} params.recommendation_issued_at
 * @param {string|null} params.recommendation_issued_by
 * @param {string|null} params.recommendation_withheld_reason
 * @returns {object} readiness_state
 */
export function createReadinessState({
  go_live_readiness = null,
  readiness_reason = null,
  incomplete_critical_checkpoints = [],
  blocked_checkpoints = [],
  deferred_checkpoints = [],
  unresolved_warnings = [],
  training_status = null,
  recommendation_issued = false,
  recommendation_issued_at = null,
  recommendation_issued_by = null,
  recommendation_withheld_reason = null,
} = {}) {
  return {
    go_live_readiness,
    readiness_reason,
    incomplete_critical_checkpoints: Array.isArray(incomplete_critical_checkpoints)
      ? incomplete_critical_checkpoints
      : [],
    blocked_checkpoints: Array.isArray(blocked_checkpoints)
      ? blocked_checkpoints
      : [],
    deferred_checkpoints: Array.isArray(deferred_checkpoints)
      ? deferred_checkpoints
      : [],
    unresolved_warnings: Array.isArray(unresolved_warnings)
      ? unresolved_warnings
      : [],
    training_status,
    recommendation_issued,
    recommendation_issued_at,
    recommendation_issued_by,
    recommendation_withheld_reason,
  };
}

// ---------------------------------------------------------------------------
// Internal: derive incomplete blocking checkpoints (R3)
//
// Returns gap records for Foundational, Domain_Required, and Go_Live
// checkpoints whose status is not Complete or Deferred.
// ---------------------------------------------------------------------------

function deriveIncompleteBlockingCheckpoints(checkpoints, stageByCheckpoint) {
  const gaps = [];
  for (const cp of checkpoints) {
    if (!BLOCKING_CHECKPOINT_CLASSES.has(cp.checkpoint_class)) continue;
    if (COMPLETION_STATUSES.has(cp.status)) continue;

    gaps.push(
      createReadinessGap({
        source_type:     READINESS_GAP_SOURCE_TYPE.CHECKPOINT,
        source_id:       cp.checkpoint_id,
        checkpoint_id:   cp.checkpoint_id,
        checkpoint_class: cp.checkpoint_class,
        domain:          cp.domain,
        stage_id:        stageByCheckpoint[cp.checkpoint_id] ?? null,
        gap_reason:
          `Checkpoint ${cp.checkpoint_id} (${cp.checkpoint_class}) has status ` +
          `"${cp.status ?? "Not_Started"}" — must reach Complete or Deferred before go-live.`,
      })
    );
  }
  return gaps;
}

// ---------------------------------------------------------------------------
// Internal: derive blocked checkpoints from active blockers (R5)
//
// Only blockers on blocking-class checkpoints are readiness-blocking.
// Standard blockers on Recommended/Optional go to unresolved_warnings.
// Returns { blockingGaps, warningGaps }.
// ---------------------------------------------------------------------------

function deriveBlockerGaps(blockers, checkpointsById) {
  const blockingGaps = [];
  const warningGaps  = [];

  for (const blocker of blockers.active_blockers) {
    const cp = checkpointsById.get(blocker.source_checkpoint_id);
    const cpClass = cp?.checkpoint_class ?? null;

    if (BLOCKING_CHECKPOINT_CLASSES.has(cpClass)) {
      blockingGaps.push(
        createReadinessGap({
          source_type:     READINESS_GAP_SOURCE_TYPE.BLOCKER,
          source_id:       blocker.blocker_id,
          checkpoint_id:   blocker.source_checkpoint_id,
          checkpoint_class: cpClass,
          domain:          blocker.source_domain_id,
          stage_id:        blocker.source_stage_id ?? null,
          blocker_id:      blocker.blocker_id,
          blocker_type:    blocker.blocker_type,
          gap_reason:      blocker.blocked_reason,
        })
      );
    } else {
      // Recommended or Optional — advisory only (R4, R5)
      warningGaps.push(
        createReadinessGap({
          source_type:     READINESS_GAP_SOURCE_TYPE.BLOCKER,
          source_id:       blocker.blocker_id,
          checkpoint_id:   blocker.source_checkpoint_id,
          checkpoint_class: cpClass,
          domain:          blocker.source_domain_id,
          stage_id:        blocker.source_stage_id ?? null,
          blocker_id:      blocker.blocker_id,
          blocker_type:    blocker.blocker_type,
          gap_reason:      blocker.blocked_reason,
        })
      );
    }
  }

  return { blockingGaps, warningGaps };
}

// ---------------------------------------------------------------------------
// Internal: derive deferred checkpoints (R11)
//
// Records all Deferred checkpoints for informational tracking.
// Deferred satisfies the R2 gate — these do not block readiness by class.
// ---------------------------------------------------------------------------

function deriveDeferredCheckpoints(checkpoints, deformentsById, stageByCheckpoint) {
  const deferred = [];
  for (const cp of checkpoints) {
    if (cp.status !== "Deferred") continue;

    const deferment = deformentsById.get(cp.checkpoint_id) ?? null;
    deferred.push(
      createReadinessGap({
        source_type:     READINESS_GAP_SOURCE_TYPE.CHECKPOINT,
        source_id:       cp.checkpoint_id,
        checkpoint_id:   cp.checkpoint_id,
        checkpoint_class: cp.checkpoint_class,
        domain:          cp.domain,
        stage_id:        stageByCheckpoint[cp.checkpoint_id] ?? null,
        deferment_id:    deferment?.deferment_id ?? null,
        has_owner_signoff: deferment?.owner_signoff ?? false,
        gap_reason:
          `Checkpoint ${cp.checkpoint_id} (${cp.checkpoint_class}) is Deferred` +
          (deferment?.owner_signoff
            ? " with project-owner signoff."
            : " — project-owner signoff not recorded."),
      })
    );
  }
  return deferred;
}

// ---------------------------------------------------------------------------
// Internal: derive advisory warnings for Recommended/Optional checkpoints (R4)
// ---------------------------------------------------------------------------

function deriveAdvisoryCheckpointWarnings(checkpoints, stageByCheckpoint) {
  const warnings = [];
  for (const cp of checkpoints) {
    if (BLOCKING_CHECKPOINT_CLASSES.has(cp.checkpoint_class)) continue;
    if (COMPLETION_STATUSES.has(cp.status)) continue;

    warnings.push(
      createReadinessGap({
        source_type:     READINESS_GAP_SOURCE_TYPE.CHECKPOINT,
        source_id:       cp.checkpoint_id,
        checkpoint_id:   cp.checkpoint_id,
        checkpoint_class: cp.checkpoint_class,
        domain:          cp.domain,
        stage_id:        stageByCheckpoint[cp.checkpoint_id] ?? null,
        gap_reason:
          `Checkpoint ${cp.checkpoint_id} (${cp.checkpoint_class}) has status ` +
          `"${cp.status ?? "Not_Started"}" — does not block go-live but is unresolved.`,
      })
    );
  }
  return warnings;
}

// ---------------------------------------------------------------------------
// Internal: derive training gaps (R8)
//
// Training blocks readiness only when projectState.project_identity
// .required_training_confirmed is explicitly false.
// Otherwise training gaps are advisory warnings only.
// Returns { trainingBlockingGaps, trainingAdvisoryGaps, training_status }.
// ---------------------------------------------------------------------------

function deriveTrainingGaps(checkpoints, stageByCheckpoint, projectState) {
  // Training availability is tracked independently of checkpoint completion status.
  // A checkpoint may be Complete (configuration done) while training remains
  // unconfirmed. All checkpoints with training_available = true are reported.
  const trainingAvailableCheckpoints = checkpoints.filter(
    (cp) => cp.training_available === true
  );

  const training_status = trainingAvailableCheckpoints.length === 0
    ? READINESS_TRAINING_STATUS.OK
    : READINESS_TRAINING_STATUS.AVAILABLE_NOT_CONFIRMED;

  // Explicit project-owner required-training signal (R8)
  const requiredTrainingSignal =
    projectState?.project_identity?.required_training_confirmed === false;

  const trainingBlockingGaps = [];
  const trainingAdvisoryGaps = [];

  for (const cp of trainingAvailableCheckpoints) {
    const gap = createReadinessGap({
      source_type:     READINESS_GAP_SOURCE_TYPE.TRAINING,
      source_id:       cp.checkpoint_id,
      checkpoint_id:   cp.checkpoint_id,
      checkpoint_class: cp.checkpoint_class,
      domain:          cp.domain,
      stage_id:        stageByCheckpoint[cp.checkpoint_id] ?? null,
      gap_reason: requiredTrainingSignal
        ? `Training required by project owner for checkpoint ${cp.checkpoint_id} ` +
          `(${cp.checkpoint_class}) has not been confirmed.`
        : `Checkpoint ${cp.checkpoint_id} (${cp.checkpoint_class}) has training ` +
          `available — advisory unless project owner marks training as required.`,
    });

    if (requiredTrainingSignal) {
      trainingBlockingGaps.push(gap);
    } else {
      trainingAdvisoryGaps.push(gap);
    }
  }

  return { trainingBlockingGaps, trainingAdvisoryGaps, training_status };
}

// ---------------------------------------------------------------------------
// Internal: derive target-context / environment operational gaps (R6, R7, R9)
//
// Only fires when deployment_type === "odoosh" AND the relevant field is
// present. Returns gap records or empty array.
// ---------------------------------------------------------------------------

function deriveEnvironmentGaps(targetContext) {
  if (targetContext === null || targetContext === undefined) return [];
  if (targetContext.deployment_type !== "odoosh") return [];

  const gaps = [];

  // Non-production environment check (R9): only when field is explicitly present
  const envType = targetContext.odoosh_environment_type;
  if (envType !== null && envType !== undefined && NON_PRODUCTION_ODOOSH_ENV_TYPES.has(envType)) {
    gaps.push(
      createReadinessGap({
        source_type: READINESS_GAP_SOURCE_TYPE.TARGET_CONTEXT,
        source_id:   "odoosh_environment_type",
        gap_reason:
          `Target environment is "${envType}" — production readiness cannot be ` +
          `confirmed from non-production evidence alone. ` +
          `Transition to production environment required before go-live.`,
      })
    );
  }

  // Branch target check (R7): only when deployment_type is odoosh
  const branchTarget = targetContext.odoosh_branch_target;
  if (branchTarget === null || branchTarget === undefined) {
    gaps.push(
      createReadinessGap({
        source_type: READINESS_GAP_SOURCE_TYPE.TARGET_CONTEXT,
        source_id:   "odoosh_branch_target",
        gap_reason:
          `Odoo.sh branch target is not defined — production branch must be ` +
          `set before go-live.`,
      })
    );
  }

  return gaps;
}

// ---------------------------------------------------------------------------
// Internal: readiness_reason string
// ---------------------------------------------------------------------------

function buildReadinessReason(status, configGapCount, operationalGapCount, warningCount) {
  if (status === GO_LIVE_READINESS_STATUS.NOT_READY) {
    return (
      `Go-live readiness not achieved: ${configGapCount} configuration gap(s) identified. ` +
      `See incomplete_critical_checkpoints and blocked_checkpoints for details.`
    );
  }
  if (status === GO_LIVE_READINESS_STATUS.CONFIGURATION_COMPLETE_NOT_OPERATIONALLY_READY) {
    return (
      `Configuration is complete but operational readiness cannot be confirmed: ` +
      `${operationalGapCount} operational gap(s) identified. ` +
      `See incomplete_critical_checkpoints for target-context and training details.`
    );
  }
  // Ready
  return warningCount > 0
    ? `Go-live readiness achieved. ${warningCount} unresolved warning(s) noted — these do not block go-live.`
    : "Go-live readiness achieved. All blocking gates passed.";
}

// ---------------------------------------------------------------------------
// Main export: computeGoLiveReadiness
// ---------------------------------------------------------------------------

/**
 * Derives go-live readiness state from governed inputs only.
 *
 * Does NOT:
 *   - validate checkpoints
 *   - recompute blockers
 *   - generate previews or execution candidates
 *   - invent missing evidence
 *   - infer acceptance
 *   - claim production readiness from non-production evidence
 *
 * @param {Array}       checkpoints            - array of checkpoint records (persisted state)
 * @param {object}      validatedCheckpoints   - output of computeValidation()
 *                                               { records, engine_version, validated_at }
 * @param {object}      blockers               - output of computeBlockers()
 *                                               { active_blockers, total_count, ... }
 * @param {object}      stageRouting           - output of computeStageRouting()
 *                                               { checkpoint_to_stage, ... }
 * @param {object|null} [projectState]         - optional; reads target_context and deferments
 *                                               only where explicitly required
 * @returns {object} readiness_state matching §1.8 of the runtime contract
 */
export function computeGoLiveReadiness(
  checkpoints,
  validatedCheckpoints,
  blockers,
  stageRouting,
  projectState = null
) {
  if (!Array.isArray(checkpoints)) {
    throw new Error("computeGoLiveReadiness: checkpoints must be an array.");
  }
  if (
    validatedCheckpoints === null ||
    typeof validatedCheckpoints !== "object" ||
    !Array.isArray(validatedCheckpoints.records)
  ) {
    throw new Error(
      "computeGoLiveReadiness: validatedCheckpoints must be an object with a records array."
    );
  }
  if (
    blockers === null ||
    typeof blockers !== "object" ||
    !Array.isArray(blockers.active_blockers)
  ) {
    throw new Error(
      "computeGoLiveReadiness: blockers must be an object with an active_blockers array."
    );
  }
  if (stageRouting === null || typeof stageRouting !== "object") {
    throw new Error("computeGoLiveReadiness: stageRouting must be an object.");
  }

  // --- Build lookup maps ---
  const checkpointsById     = new Map(checkpoints.map((cp) => [cp.checkpoint_id, cp]));
  const stageByCheckpoint   = stageRouting.checkpoint_to_stage ?? {};
  const targetContext       = projectState?.target_context ?? null;
  const rawDeferments       = Array.isArray(projectState?.deferments) ? projectState.deferments : [];
  // Index deferments by source_checkpoint_id (last-write-wins for duplicates)
  const defermentsByCheckpointId = new Map(
    rawDeferments
      .filter((d) => d.source_checkpoint_id !== null && d.source_checkpoint_id !== undefined)
      .map((d) => [d.source_checkpoint_id, d])
  );

  // --- Gate A: Incomplete blocking checkpoints (R3) ---
  const incompleteBlockingGaps = deriveIncompleteBlockingCheckpoints(
    checkpoints,
    stageByCheckpoint
  );

  // --- Gate B: Active blockers on blocking-class checkpoints (R5) ---
  const { blockingGaps: blockerBlockingGaps, warningGaps: blockerWarningGaps } =
    deriveBlockerGaps(blockers, checkpointsById);

  // --- Gate C: Required training (R8) ---
  const { trainingBlockingGaps, trainingAdvisoryGaps, training_status } =
    deriveTrainingGaps(checkpoints, stageByCheckpoint, projectState);

  // --- Gate D: Target-context / environment operational gaps (R6, R7, R9) ---
  const environmentGaps = deriveEnvironmentGaps(targetContext);

  // --- Informational: deferred checkpoints (R11) ---
  const deferredCheckpoints = deriveDeferredCheckpoints(
    checkpoints,
    defermentsByCheckpointId,
    stageByCheckpoint
  );

  // --- Informational: advisory warnings for Recommended/Optional (R4) ---
  const advisoryCheckpointWarnings = deriveAdvisoryCheckpointWarnings(
    checkpoints,
    stageByCheckpoint
  );

  // --- Determine readiness status (R10) ---
  //
  // Configuration gaps: incomplete blocking checkpoints + critical blockers +
  //                     explicitly-required training gaps
  // Operational gaps:   environment/branch target gaps
  //
  // Configuration complete = no config gaps
  // Operationally ready    = configuration complete AND no operational gaps

  const configGapCount      = incompleteBlockingGaps.length + blockerBlockingGaps.length + trainingBlockingGaps.length;
  const operationalGapCount = environmentGaps.length;
  const configurationComplete = configGapCount === 0;
  const operationallyReady    = configurationComplete && operationalGapCount === 0;

  let go_live_readiness;
  if (!configurationComplete) {
    go_live_readiness = GO_LIVE_READINESS_STATUS.NOT_READY;
  } else if (!operationallyReady) {
    go_live_readiness = GO_LIVE_READINESS_STATUS.CONFIGURATION_COMPLETE_NOT_OPERATIONALLY_READY;
  } else {
    go_live_readiness = GO_LIVE_READINESS_STATUS.READY;
  }

  // --- Assemble incomplete_critical_checkpoints ---
  // Includes: config checkpoint gaps, required-training gaps,
  // and operational environment gaps (all are "critical" in the sense
  // that they block the respective readiness tier).
  const incomplete_critical_checkpoints = [
    ...incompleteBlockingGaps,
    ...trainingBlockingGaps,
    ...environmentGaps,
  ];

  // --- Assemble unresolved_warnings ---
  const unresolved_warnings = [
    ...advisoryCheckpointWarnings,
    ...blockerWarningGaps,
    ...trainingAdvisoryGaps,
  ];

  const warningCount = unresolved_warnings.length;
  const readiness_reason = buildReadinessReason(
    go_live_readiness,
    configGapCount,
    operationalGapCount,
    warningCount
  );

  const recommendation_withheld_reason =
    go_live_readiness !== GO_LIVE_READINESS_STATUS.READY ? readiness_reason : null;

  return createReadinessState({
    go_live_readiness,
    readiness_reason,
    incomplete_critical_checkpoints,
    blocked_checkpoints:    blockerBlockingGaps,
    deferred_checkpoints:   deferredCheckpoints,
    unresolved_warnings,
    training_status,
    recommendation_issued:       false,
    recommendation_issued_at:    null,
    recommendation_issued_by:    null,
    recommendation_withheld_reason,
  });
}
